<?php
// Identity
$a = false;
echo +$a . PHP_EOL;

// Negation
$a = -10;
echo -$a . PHP_EOL;

// Addition
$a = $b = 10;
echo $a + $b . PHP_EOL;

// Subtraction
echo $a - $b . PHP_EOL;

// Multiplication
echo $a * $b . PHP_EOL;

// Division
echo $a / $b . PHP_EOL;

// Modulus
echo $a % $b . PHP_EOL;

// Exponential
echo $a**$b . PHP_EOL;
